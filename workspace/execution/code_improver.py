"""
Deterministic static code analysis tool.

Scans source files for readability, performance, and security issues.
Outputs structured reports (JSON or Markdown) for LLM orchestration.

Usage:
    python code_improver.py <path> [--format json|markdown] [--severity low|medium|high]
                                   [--category readability|performance|security]
                                   [--exclude patterns]
"""

