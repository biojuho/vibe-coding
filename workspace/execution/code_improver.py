def detect_language(file_path: str) -> Optional[str]:
    ext = Path(file_path).suffix.lower()
    return LANGUAGE_MAP.get(ext)


# [FIX #1] Added OSError handling for permission denied / locked files
def read_file_safe(file_path: str) -> Optional[str]:
    for enc in ("utf-8", "latin-1"):
        try:
            return Path(file_path).read_text(encoding=enc)
        except (UnicodeDecodeError, ValueError):
            continue
        except OSError:
            return None
    return None


def is_binary(file_path: str) -> bool:
    try:
        with open(file_path, "rb") as f:
            chunk = f.read(8192)
        return b"\x00" in chunk
    except OSError:
        return True


def is_minified(source: str) -> bool:
    lines = source.split("\n")
    if len(lines) < 3:
        return any(len(l) > 5000 for l in lines)
    avg_len = sum(len(l) for l in lines) / max(len(lines), 1)
    return avg_len > 500


def is_generated(source: str) -> bool:
    header = source[:1000].lower()
    markers = [
        "generated by",
        "auto-generated",
        "@generated",
        "do not edit",
        "automatically generated",
        "this file is generated",
    ]
    return any(m in header for m in markers)


def is_generated_filename(file_path: str) -> bool:
    name = Path(file_path).name
    if name.lower().startswith("workbox-"):
        return True
    return bool(HASHED_BUNDLE_PATTERN.match(name))


def should_skip(file_path: str, source: str, max_size: int) -> Tuple[bool, str]:
    try:
        size = os.path.getsize(file_path)
    except OSError:
        return True, "Cannot access file"
    if size > max_size:
        return True, f"File too large ({size} bytes > {max_size})"
    if is_binary(file_path):
        return True, "Binary file"
    if not source or not source.strip():
        return True, "Empty file"
    if is_minified(source):
        return True, "Minified file"
    if is_generated(source):
        return True, "Generated file"
    if is_generated_filename(file_path):
        return True, "Generated bundle file"
    return False, ""


def extract_snippet(lines: List[str], line_num: int, context: int = 0) -> str:
    idx = line_num - 1
    start = max(0, idx - context)
    end = min(len(lines), idx + context + 1)
    return "\n".join(lines[start:end]).rstrip()


def _find_embedded_credential_url(line: str) -> Optional[str]:
    """Return a URL with userinfo (username/password) embedded in the authority if found."""
    for match in URL_CANDIDATE_PATTERN.finditer(line):
        candidate = match.group(0).rstrip(".,);]")
        try:
            parsed = urlsplit(candidate)
        except ValueError:
            continue
        if parsed.username is not None or parsed.password is not None:
            return candidate
    return None


# [FIX #2] Proper escape char handling + comment detection
def _in_comment_or_string(line: str, match_start: int, comment_prefix: str = "#") -> bool:
    """Check if match_start position is inside a comment or string literal."""
    stripped = line.lstrip()
    if stripped.startswith(comment_prefix):
        return True
    in_str = False
    str_char = None
    i = 0
    while i < len(line):
        if i >= match_start and not in_str:
            return False
        c = line[i]
        if c == "\\" and in_str:
            i += 2  # skip escaped character
            continue
        if c in ('"', "'") and not in_str:
            in_str = True
            str_char = c
        elif c == str_char and in_str:
            in_str = False
        i += 1
    return in_str


