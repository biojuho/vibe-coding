class _ComplexityVisitor(ast.NodeVisitor):
    def __init__(self):
        self.complexity = 1

    def visit_If(self, node):
        self.complexity += 1
        self.generic_visit(node)

    def visit_For(self, node):
        self.complexity += 1
        self.generic_visit(node)

    def visit_While(self, node):
        self.complexity += 1
        self.generic_visit(node)

    def visit_ExceptHandler(self, node):
        self.complexity += 1
        self.generic_visit(node)

    def visit_With(self, node):
        self.complexity += 1
        self.generic_visit(node)

    def visit_BoolOp(self, node):
        self.complexity += len(node.values) - 1
        self.generic_visit(node)

    def visit_IfExp(self, node):
        self.complexity += 1
        self.generic_visit(node)

    def visit_comprehension(self, node):
        self.complexity += 1
        self.complexity += len(node.ifs)
        self.generic_visit(node)


def _python_cyclomatic(func_node: ast.AST) -> int:
    v = _ComplexityVisitor()
    v.visit(func_node)
    return v.complexity


# [FIX #5] Helper: find first code line (skip module docstring / comments)
def _first_code_line(source: str) -> int:
    """Return the 1-indexed line number of the first def/class/import statement."""
    for i, line in enumerate(source.split("\n"), 1):
        stripped = line.strip()
        if (
            stripped
            and not stripped.startswith("#")
            and not stripped.startswith('"""')
            and not stripped.startswith("'''")
            and not stripped.startswith('"')
            and not stripped.startswith("'")
        ):
            if any(stripped.startswith(kw) for kw in ("def ", "class ", "import ", "from ", "@")):
                return i
    return 1


# ---------------------------------------------------------------------------
# Base Analyzer (cross-language rules)
# ---------------------------------------------------------------------------


class BaseAnalyzer:
    language = "generic"
    comment_chars = "#"

    def analyze(self, source: str, file_path: str) -> Tuple[List[Issue], FileMetrics]:
        lines = source.split("\n")
        issues = []
        issues.extend(self._check_line_length(lines, file_path))
        issues.extend(self._check_file_length(lines, file_path))
        issues.extend(self._check_todo_fixme(lines, file_path))
        issues.extend(self._check_hardcoded_credentials(lines, file_path))
        metrics = FileMetrics(
            total_lines=len(lines),
            code_lines=sum(1 for l in lines if l.strip() and not l.strip().startswith(self.comment_chars)),
        )
        return issues, metrics

    def _check_line_length(self, lines, fp, limit=120):
        issues = []
        for i, line in enumerate(lines, 1):
            if len(line) > limit:
                issues.append(
                    Issue(
                        file=fp,
                        line=i,
                        column=limit,
                        category="readability",
                        severity="low",
                        rule_id="GEN001",
                        message=f"Line is {len(line)} characters (limit {limit}).",
                        code_snippet=line[:140] + "..." if len(line) > 140 else line,
                        suggestion_hint="Break long lines for readability.",
                    )
                )
        return issues

    def _check_file_length(self, lines, fp, limit=500):
        if len(lines) > limit:
            return [
                Issue(
                    file=fp,
                    line=1,
                    column=0,
                    category="readability",
                    severity="low",
                    rule_id="GEN005",
                    message=f"File has {len(lines)} lines (>{limit}). Consider splitting.",
                    code_snippet="",
                    suggestion_hint="Split into smaller modules.",
                )
            ]
        return []

    # [FIX #6] Only match TODO/FIXME in actual comments, not string literals
    def _check_todo_fixme(self, lines, fp):
        issues = []
        if self.comment_chars == "#":
            pattern = re.compile(r"^\s*#\s*(TODO|FIXME|HACK|XXX)\b", re.IGNORECASE)
        else:
            pattern = re.compile(r"^\s*(?://|/\*)\s*(TODO|FIXME|HACK|XXX)\b", re.IGNORECASE)
        for i, line in enumerate(lines, 1):
            m = pattern.search(line)
            if m:
                issues.append(
                    Issue(
                        file=fp,
                        line=i,
                        column=m.start(),
                        category="readability",
                        severity="medium",
                        rule_id="GEN003",
                        message=f"Unresolved {m.group(0)} comment found.",
                        code_snippet=line.strip(),
                        suggestion_hint="Resolve or track in issue tracker.",
                    )
                )
        return issues

    def _check_hardcoded_credentials(self, lines, fp):
        issues = []
        for i, line in enumerate(lines, 1):
            secret_detected = False
            for pat in SECRET_PATTERNS:
                if pat.search(line):
                    issues.append(
                        Issue(
                            file=fp,
                            line=i,
                            column=0,
                            category="security",
                            severity="high",
                            rule_id="GEN004",
                            message="Possible hardcoded secret/credential.",
                            code_snippet=line.strip(),
                            suggestion_hint="Use environment variables or a secrets manager.",
                        )
                    )
                    secret_detected = True
                    break
            if secret_detected:
                continue

            credential_url = _find_embedded_credential_url(line)
            if credential_url:
                issues.append(
                    Issue(
                        file=fp,
                        line=i,
                        column=0,
                        category="security",
                        severity="high",
                        rule_id="GEN004",
                        message="URL contains embedded credentials.",
                        code_snippet=credential_url,
                        suggestion_hint="Remove credentials from URLs; use auth headers.",
                    )
                )
        return issues


